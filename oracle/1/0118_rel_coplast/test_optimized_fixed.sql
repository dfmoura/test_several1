        ELSE 'N'
    END) AS CONCILIADO,
    
    (CASE WHEN (SELECT COUNT(*) FROM TCBINT T WITH (NOLOCK) WHERE T.ORIGEM IN ('F','B') AND T.NUNICO = TGFFIN.NUFIN) > 0 THEN 'S' ELSE 'N' END) AS CONTABILIZADO,
    
    (SELECT CAB.DHAPROVACAO FROM TGFCABVM CAB WITH (NOLOCK), TGFFINVM FIN WITH (NOLOCK) 
     WHERE TGFFIN.NUFIN = FIN.NUFIN AND FIN.NUNOTA = CAB.NUNOTA) AS DHAPROVACAOVENDAMAIS,
     
