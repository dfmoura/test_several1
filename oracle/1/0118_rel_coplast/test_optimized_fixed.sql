    (CASE WHEN TGFFIN.DTVENCINIC IS NOT NULL AND TGFFIN.DTNEG IS NOT NULL THEN DATEDIFF(DAY, TGFFIN.DTNEG, TGFFIN.DTVENCINIC) ELSE NULL END) AS PRAZOINICIAL,

