                ELSE DATEDIFF(DAY, CONVERT(date, TGFFIN.DTVENC), CONVERT(date, GETDATE()))
            END)
    END) AS ATRASO,
    
    (CASE 
        WHEN TGFFIN.DTVENCINIC IS NULL THEN 0 
        ELSE
            (CASE 
