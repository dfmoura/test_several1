        CASE WHEN TGFFIN.DHBAIXA IS NULL THEN 
            CASE WHEN DATEADD(DAY, DATEDIFF(DAY, 0, TGFFIN.DTVENC), 0) < DATEADD(DAY, DATEDIFF(DAY, 0, GETDATE()), 0) THEN 
